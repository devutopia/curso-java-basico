package com.kleber.javabasico.aula46.labs.exer01;

public class Figura3D extends FiguraGeometrica {

	private String aresta;
	private double volume;
	
	/**
	 * @return the aresta
	 */
	public String getAresta() {
		return aresta;
	}
	/**
	 * @param aresta the aresta to set
	 */
	public void setAresta(String aresta) {
		this.aresta = aresta;
	}
	/**
	 * @return the volume
	 */
	public double getVolume() {
		return volume;
	}
	/**
	 * @param volume the volume to set
	 */
	public void setVolume(double volume) {
		this.volume = volume;
	}
}
